import java.awt.*;

public class Tilemap {
	private int x;
	private int y;
	private boolean value;
	
	//getters
	public int getX() {
		return this.x;
	}
	public int getY() {
		return this.y;
	}
	public boolean getValue(){
		return this.value;
	}
	
	//setters
	public void setX(int x) {
		this.x = x;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	public void setValue(boolean value) {
		this.value = value;
	}
	
	
	
	
}//Tilemap